    private void displayInformation() {
       try
       {
        txtName.setText(person.getName());
        txtGeography.setText(person.getGeographicData());
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyy");  
        String strDate = dateFormat.format(person.getDateOfBirthDate());  
        txtDateOfBirth.setText(strDate);
        if(person.getTelephoneNumbers()[1]!=null && !person.getTelephoneNumbers()[1].trim().isEmpty())
        {
            txtTelephoneNumber.setText(String.valueOf(person.getTelephoneNumbers()[0]+" , "+ person.getTelephoneNumbers()[1]));
        }
        else{
            txtTelephoneNumber.setText(String.valueOf(person.getTelephoneNumbers()[0]));
        }
        txtFaxNumber.setText(String.valueOf(person.getFaxNumber()));
        if(person.getEmailAddresses()[1]!=null && !person.getEmailAddresses()[1].trim().isEmpty())
        {
            txtEmailAddress.setText(person.getEmailAddresses()[0]+" , "+ person.getEmailAddresses()[1]);
        }
        else{
            txtEmailAddress.setText(person.getEmailAddresses()[0]);
        }
        txtSSN.setText(person.getSocialSecurityNumber());
        txtMedicalRecordNumber.setText(String.valueOf(person.getMedicalRecordNumber()));
        txtHealthPlanNumber.setText(String.valueOf(person.getHealthPlanBenificiaryNumber()));
        if(String.valueOf(person.getBankAccountNumbers()[1]).trim().isEmpty())
        {
            txtBankAccountNumber.setText(String.valueOf(person.getBankAccountNumbers()[0]+" , "+ person.getBankAccountNumbers()[1]));
        }
        else
        {
            txtBankAccountNumber.setText(String.valueOf(person.getBankAccountNumbers()[0]));
        }
        txtLicenseNumber.setText(String.valueOf(person.getLicenseNumber()));
        if(String.valueOf(person.getVehicleIdentifierNumber()[1]).trim().isEmpty()){
            txtVehicleIdentifierNumber.setText(String.valueOf(person.getVehicleIdentifierNumber()[0] + " , " + person.getVehicleIdentifierNumber()[1]));
        }
        else{
            txtVehicleIdentifierNumber.setText(String.valueOf(person.getVehicleIdentifierNumber()[0]));
        }
        if(String.valueOf(person.getDeviceIdentifiers()[1]).trim().isEmpty()){
            txtDevieIdentifier.setText(person.getDeviceIdentifiers()[0] + " , " + person.getDeviceIdentifiers()[1]);
        }
        else{
            txtDevieIdentifier.setText(person.getDeviceIdentifiers()[0]);
        }
        txtLinkedinId.setText(person.getLinkedinId());
        txtIPPart.setText(person.getIpAddress());
        lblBiometric.setBounds(10,10,670,250);
        lblFullFace.setBounds(10,10,670,250);
        lblBiometric.setIcon(ResizeBiometricImage(person.getBiometricIdentifierUrl()));
        lblFullFace.setIcon(ResizeFacePhotoImage(person.getFacePhotoUrl()));
        txtApplicationGuid.setText(person.getUniqueIdentifier().toString());
    }
    catch(HeadlessException | NumberFormatException ex)
    {
        JOptionPane.showMessageDialog(this,"Information Not Saved due to exception");
        throw ex;
    }
    }
     public ImageIcon ResizeBiometricImage(String ImagePath)
    {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(lblBiometric.getWidth(), lblBiometric.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
     public ImageIcon ResizeFacePhotoImage(String ImagePath)
    {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(lblFullFace.getWidth(), lblFullFace.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
